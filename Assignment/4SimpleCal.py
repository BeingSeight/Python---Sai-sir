# Prompt the user for two numbers and display their sum, difference, product, and quotient.
num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))
print("Sum of given two numbers is " + str(num1 + num2))
print("Difference of given two numbers is " + str(num1 - num2))
print("Product of given two numbers is " + str(num1 * num2))
print("Quotient of given two numbers is " + str(num1 / num2))